// Helper functions
// Sắp xếp danh sách tỉnh/thành phố theo alphabet A-Z
const sortedProvinces = [...vietnamAddresses].sort((a, b) => {
  // Loại bỏ "Tỉnh" hoặc "Thành phố" để so sánh
  const nameA = a.name.replace(/^(Tỉnh|Thành phố)\s+/, "").toLowerCase();
  const nameB = b.name.replace(/^(Tỉnh|Thành phố)\s+/, "").toLowerCase();
  return nameA.localeCompare(nameB, "vi");
});

export const getProvinces = () => sortedProvinces.map((p) => ({ code: p.code, name: p.name }));

export const getDistrictsByProvince = (provinceCode: string) => {
  const province = vietnamAddresses.find((p) => p.code === provinceCode);
  if (!province) return [];
  
  // Sắp xếp quận/huyện theo alphabet
  const sortedDistricts = [...province.districts].sort((a, b) => {
    const nameA = a.name.replace(/^(Thành phố|Thị xã|Huyện|Quận)\s+/, "").toLowerCase();
    const nameB = b.name.replace(/^(Thành phố|Thị xã|Huyện|Quận)\s+/, "").toLowerCase();
    return nameA.localeCompare(nameB, "vi");
  });
  
  return sortedDistricts.map((d) => ({ code: d.code, name: d.name }));
};

export const getWardsByDistrict = (provinceCode: string, districtCode: string) => {
  const province = vietnamAddresses.find((p) => p.code === provinceCode);
  if (!province) return [];
  const district = province.districts.find((d) => d.code === districtCode);
  if (!district) return [];
  
  // Sắp xếp phường/xã theo alphabet
  const sortedWards = [...district.wards].sort((a, b) => {
    const nameA = a.name.replace(/^(Phường|Xã|Thị trấn)\s+/, "").toLowerCase();
    const nameB = b.name.replace(/^(Phường|Xã|Thị trấn)\s+/, "").toLowerCase();
    return nameA.localeCompare(nameB, "vi");
  });
  
  return sortedWards;
};

